    private void SignUpBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SignUpBtnMouseClicked
        if(txtNama.getText().isEmpty() || txtUsername.getText().isEmpty() || txtPassword.getText().isEmpty() || txtCPassword.getText().isEmpty() )
        {
            JOptionPane.showMessageDialog(this, "Missing Information");
        }else if(!txtPassword.getText().equals(txtCPassword.getText())){
            
        }else{
            try {
            String nama = txtNama.getText();
            String username = txtUsername.getText();
            String password = txtPassword.getText();
            SignUp sg = new SignUp(nama, username, password);
            SignUp.DaftarAcc(sg);
            Clear();
            
            JOptionPane.showMessageDialog(this, "Registrasi Sukses");
  
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
        new JLogin().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_SignUpBtnMouseClicked

