    private void LoginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginBtnActionPerformed
        String username = UnameTb.getText();
        String password = PasswordTb.getText();
        
        if (UnameTb.getText().isEmpty() || PasswordTb.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Enter username and PIN");
        }else{
            // Memeriksa apakah pengguna adalah admin
            boolean isAdmin = Login.isAdmin(username, password);

            if (isAdmin) {
                // Pengguna adalah admin, tampilkan tampilan admin
                new JmainMenu().setVisible(true);
                dispose(); // Menutup frame login
            } else {
                Login.LoginMhs(UnameTb, PasswordTb);
                // Pengguna bukan admin, tampilkan pesan kesalahan
            }
                
            } 
        
    }//GEN-LAST:event_LoginBtnActionPerformed

